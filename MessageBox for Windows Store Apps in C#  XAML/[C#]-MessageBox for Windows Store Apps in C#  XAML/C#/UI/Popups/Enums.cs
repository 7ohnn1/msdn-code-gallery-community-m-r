
// Conatins the enums defined in the namespace
namespace InstanceFactory.MessageBoxSample.UI.Popups
{
  /// <summary>
  /// Specifies constants defining which symbol to display by the <see cref="MessageBox"/>.
  /// </summary>
  public enum MessageBoxSymbol
  {
    /// <summary>
    /// Show an error symbol.
    /// </summary>
    Error,

    /// <summary>
    /// Show an information symbol.
    /// </summary>
    Information,

    /// <summary>
    /// Show no symbol
    /// </summary>
    None,

    /// <summary>
    /// Show a warning symbol.
    /// </summary>
    Warning
  }
}
