namespace RCE.Infrastructure.Models
{
    using System;

    public class KeyboardMapping : IEquatable<KeyboardMapping>
    {
        public KeyboardMapping()
        {
        }

        public KeyboardMapping(string name, string url)
        {
            this.Name = name;
            this.Url = url;
        }

        public string Name { get; set; }

        public string Url { get; set; }

        /// <summary>
        /// Determines whether the specified <see cref="T:System.Object"/> is equal to the current <see cref="T:System.Object"/>.
        /// </summary>
        /// <returns>
        /// true if the specified <see cref="T:System.Object"/> is equal to the current <see cref="T:System.Object"/>; otherwise, false.
        /// </returns>
        /// <param name="obj">The <see cref="T:System.Object"/> to compare with the current <see cref="T:System.Object"/>. </param>
        public override bool Equals(object obj)
        {
            if (obj == null)
            {
                return false;
            }

            if (this.GetType() != obj.GetType())
            {
                return false;
            }

            return this.Equals((KeyboardMapping)obj);
        }

        /// <summary>
        /// Indicates whether the current object is equal to another object of the same type.
        /// </summary>
        /// <returns>
        /// true if the current object is equal to the <paramref name="other"/> parameter; otherwise, false.
        /// </returns>
        /// <param name="other">An object to compare with this object.</param>
        public bool Equals(KeyboardMapping other)
        {
            if (other == null)
            {
                return false;
            }

            if (ReferenceEquals(this, other))
            {
                return true;
            }

            return this.Name.Equals(other.Name) && this.Url.Equals(other.Url);
        }

        /// <summary>
        /// Serves as a hash function for a particular type. 
        /// </summary>
        /// <returns>
        /// A hash code for the current <see cref="T:System.Object"/>.
        /// </returns>
        public override int GetHashCode()
        {
            int hash = 17;
            hash = (hash * 23) + (this.Name ?? string.Empty).GetHashCode();
            hash = (hash * 23) + (this.Url ?? string.Empty).GetHashCode();

            return hash;
        }
    }
}